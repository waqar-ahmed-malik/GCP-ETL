CREATE OR REPLACE TABLE LANDING.WORK_MDM_TRAVEL AS 
SELECT DISTINCT 
CAST(TRIP_TRANSACTION_NUM AS STRING) as SOURCE_KEY1,
CAST(NULL AS STRING) as SOURCE_KEY2,
CAST(NULL AS STRING) as SOURCE_KEY3,
'TRIP_TRANSACTION_NUM' AS SOURCE_KEY1_COLUMN_NAME,
CAST(NULL AS STRING) AS SOURCE_KEY2_COLUMN_NAME,	
CAST(NULL AS STRING) AS SOURCE_KEY3_COLUMN_NAME,
SOURCE_SYSTEM_CD AS SOURCE_SYSTEM,
TRAVELER_FIRST_NM AS FIRST_NM,
TRAVELER_LAST_NM AS LAST_NM,
CAST(NULL AS STRING) AS NAME_SUFFIX_CD,
CAST(NULL AS STRING) AS SALUTATION_CD,
NULL AS BIRTH_DATE,
CAST(NULL AS STRING) AS GENDER_CD,
CAST(NULL AS STRING) AS MARITAL_STATUS,
TRAVELER_ADDRESS_LINE_1 AS PRIMARY_ADDRESS_LINE1,
TRAVELER_ADDRESS_LINE_2 AS PRIMARY_ADDRESS_LINE2,
TRAVELER_CITY AS PRIMARY_CITY,
TRAVELER_STATE AS PRIMARY_STATE,
TRAVELER_ZIP_CD AS PRIMARY_ZIP,
TRAVELER_EMAIL_ADDRESS AS PRIMARY_EMAIL_ADDRESS,
TRAVELER_ADDRESS_LINE_1 AS RESIDENTIAL_ADDRESS_LINE1,
TRAVELER_ADDRESS_LINE_2 AS RESIDENTIAL_ADDRESS_LINE2,
TRAVELER_CITY AS RESIDENTIAL_CITY,
TRAVELER_STATE AS RESIDENTIAL_STATE,
TRAVELER_ZIP_CD AS RESIDENTIAL_ZIP,
CAST(NULL AS STRING) AS BILLING_ADDRESS_LINE1,
CAST(NULL AS STRING) AS BILLING_ADDRESS_LINE2,
CAST(NULL AS STRING) AS BILLING_CITY,
CAST(NULL AS STRING) AS BILLING_STATE,
CAST(NULL AS STRING) AS BILLING_ZIP,
CAST(NULL AS STRING) AS OTHER_ADDRESS_LINE1,
CAST(NULL AS STRING) AS OTHER_ADDRESS_LINE2,
CAST(NULL AS STRING) AS OTHER_CITY,
CAST(NULL AS STRING) AS OTHER_STATE,
CAST(NULL AS STRING) AS OTHER_ZIP,
CAST(NULL AS STRING) AS OTHER_EMAIL_ADDRESS,
TRAVELER_PHONE_NUM AS HOME_TELEPHONE_NUM,
CAST(NULL AS STRING) AS WORK_PHONE_NUM,
CAST(NULL AS STRING) AS OTHER_PHONE_NUM,
CREATE_DT AS SOURCE_CREATE_DATE
FROM
(
SELECT
  SOURCE_TRANSACTION_ID,
  CAST(TRIP_TRANSACTION_NUM AS STRING) AS TRIP_TRANSACTION_NUM,
  UPPER(TRAVELER_FIRST_NM) AS TRAVELER_FIRST_NM,
  UPPER(TRAVELER_LAST_NM) AS TRAVELER_LAST_NM,
  TRAVELER_ADDRESS_LINE_1,
  TRAVELER_ADDRESS_LINE_2,
  TRAVELER_CITY,
  TRAVELER_STATE,
  TRAVELER_ZIP_CD,
  TRAVELER_PHONE_NUM,
  TRAVELER_EMAIL_ADDRESS,
  SOURCE_SYSTEM_CD,
  PARSE_DATE('%Y-%m-%d',SUBSTR(CAST(CREATE_DTTIME AS STRING),1,10)) AS CREATE_DT
FROM
  LANDING.WORK_TRAVEL_TST_TRANSACTIONS)