MERGE `OPERATIONAL.AMS_RS_IMAGE` AS TGT
USING `LANDING.WORK_AMS_RS_IMAGE` AS SRC
ON TGT.UNIQUE_RS_IMAGE = SAFE_CAST(SRC.UNIQRSIMAGE AS INT64)

WHEN MATCHED
THEN UPDATE SET
TGT.UNIQUE_RS_IMAGE = SAFE_CAST(SRC.UNIQRSIMAGE AS INT64)
,TGT.UNIQUE_LINE_IMAGE = SAFE_CAST(SRC.UNIQLINEIMAGE AS INT64)
,TGT.JOB_RUN_ID = SAFE_CAST(SRC.JOBRUNID AS STRING)
--,TGT.CREATE_DTTIME = CURRENT_DATETIME()
,TGT.UPDATE_DTTIME = CURRENT_DATETIME()
--,TGT.SOURCE_SYSTEM_CD = 'AMS'

WHEN NOT MATCHED
THEN INSERT ( --Target column names
UNIQUE_RS_IMAGE
,UNIQUE_LINE_IMAGE
,JOB_RUN_ID
,CREATE_DTTIME
,UPDATE_DTTIME
,SOURCE_SYSTEM_CD
)

VALUES ( --Source values
SAFE_CAST(UNIQRSIMAGE AS INT64)
,SAFE_CAST(UNIQLINEIMAGE AS INT64)
,SAFE_CAST(JOBRUNID AS STRING)
,CURRENT_DATETIME()
,CURRENT_DATETIME()
,'AMS'
)
