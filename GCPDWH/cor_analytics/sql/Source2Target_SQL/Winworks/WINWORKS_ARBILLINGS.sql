SELECT
  PARSE_DATETIME('%d-%h-%y %H.%M.%S.000000000 %p',
    W_DATE) AS W_DATE,
  PARSE_DATETIME('%d-%h-%y %H.%M.%S.000000000 %p',
    L_DATE) AS L_DATE,
  PARSE_DATETIME('%d-%h-%y %H.%M.%S.000000000 %p',
    T_DATE) AS T_DATE,
  PARSE_DATETIME('%d-%h-%y %H.%M.%S.000000000 %p',
    S_DATE) AS S_DATE,
  CUST_ID,
  CAST(CHARGES AS FLOAT64) AS CHARGES,
  CAST(PAYMENTS AS FLOAT64) AS PAYMENTS,
  CAST(BALANCE AS FLOAT64) AS BALANCE,
  CAST(DAYS_30 AS FLOAT64) AS DAYS_30,
  CAST(DAYS_60 AS FLOAT64) AS DAYS_60,
  CAST(DAYS_90 AS FLOAT64) AS DAYS_90,
  CAST(DAYS_120 AS FLOAT64) AS DAYS_120,
  PARSE_DATETIME('%d-%h-%y %H.%M.%S.000000000 %p',
    CREATE_DTTIME) AS CREATE_DTTIME,
  CREATE_BY,
  SOURCE_SYSTEM_CD
FROM
  `aaadata-181822.COR_WINWORKS.STG_ARBILLINGS`