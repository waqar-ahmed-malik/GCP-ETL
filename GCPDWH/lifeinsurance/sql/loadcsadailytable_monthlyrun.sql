INSERT INTO LANDING.LIFE_INS_CSAPRDLY_DAILY
(
BATCH_FILE_NM,	
APPLICATION_ID,	
COMPANY_CD,	
POLICY_NUM,	
PROCESSING_DT,
PRODUCT_TYPE,	
PRODUCTION_TYPE,	
POLICY_STATUS,	
BILLING_MODE,	
PLAN_CD,	
FACE_AMT,
ANNUAL_PREMIUM,
MODAL_PREMIUM,
ANNUALIZED_PREMIUM,
AGENT_NUM,	
POLICY_SPLIT,
TARGET_PREMIUIM,
ANNUAL_NO_LAPSE_PREMIUM,
INSURED_NM,	
CLUB_CD,	
PRODUCTION_AMT,
ZIP_CD,	
JOB_RUN_ID,	
SOURCE_SYSTEM_CD,	
CREATE_DTTIME,
CREATE_BY,	
LAST_UPDATE_DTTIME,
LAST_UPDATE_BY,
MONTHLY_FLAG
)
SELECT
  CONCAT('BATCH',CAST(CURRENT_DATE() AS STRING)) AS BATCH_NAME,
  LTRIM(RTRIM(MCUSR1)) AS APPLICATION_ID,
  LTRIM(RTRIM(PDCO)) AS COMPANY_CD,
  LTRIM(RTRIM(PDPOLN)) AS POLICY_NUM,
  PARSE_DATE('%m%d%Y',CONCAT(IF(LENGTH(LTRIM(RTRIM(PDTRNM)))<>2,
      CONCAT('0',(RTRIM(PDTRNM))),
      (RTRIM(PDTRNM))),IF(LENGTH(LTRIM(RTRIM(PDTRND)))<>2,
      CONCAT('0',(RTRIM(PDTRND))),
      (RTRIM(PDTRND))),LTRIM(RTRIM(PDTRNY)))) AS PROCESSING_DT,  
  LTRIM(RTRIM(PDPRDT)) AS PRODUCT_TYPE,
  LTRIM(RTRIM(PDPTYP)) AS PRODUCTION_TYPE,
  LTRIM(RTRIM(PDNSTA)) AS POLICY_STATUS,
  LTRIM(RTRIM(PDMODE)) AS BILLING_MODE,
  LTRIM(RTRIM(PDPLAN)) AS PLAN_CD,
  PDTAMT AS FACE_AMOUNT,
  PDTPRM AS ANNUAL_PREMIUM,
  PDMPRM AS MODAL_PREMIUM,
  PDUPRM AS ANNUALIZED_PREMIUM,
  LTRIM(RTRIM(PDAGT)) AS AGENT_NUMBER,
  PDPSPT AS POLICY_SPLIT,
  PDPRMT AS TARGET_PREMIUM,
  PDNLPM AS ANNUAL_NO_LAPSE_PREMIUM,
  LTRIM(RTRIM(IBINAM)) AS INSURED_NAME,
  LPAD(LTRIM(RTRIM(CLUB)),
    3,
    '0') AS CLUB_CD,
  PC AS PRODUCTION_AMOUNT,
  LTRIM(RTRIM(ZIP)) AS ZIP_CD,
  "v_job_run_id" AS JOB_RUN_ID,
  "AAA_LIFE_INS" AS SOURCE_SYSTEM_CD,
  CURRENT_TIMESTAMP() AS CREATE_DTTIME,
  "v_job_name"AS CREATE_BY,
  CURRENT_TIMESTAMP() AS LAST_UPDATE_DTTIME,
  "v_job_name" AS LAST_UPDATE_BY,
  "Y" AS MONTHLY_FLAG
FROM
  LANDING.WORK_LIFE_INS_CSAPRDLY_DAILY