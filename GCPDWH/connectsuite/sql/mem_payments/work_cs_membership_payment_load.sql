 --CONNECTSUITE_MEMBERSHIP_PAYMENT
 SELECT TRIM(CONVERT(CHAR,MPMT_KY)) AS MEMBERSHIP_PAYMENT_KEY, 
TRIM(CONVERT(CHAR,MPMT_AT)) AS MEMBERSHIP_PAYMENT_AMT, 
 TRIM(CONVERT(CHAR,MPMT_BTC_NM)) AS MEMBERSHIP_PAYMENT_BATCH_NM, 
 TRIM(CONVERT(CHAR,MPMT_METH_CD)) AS MEMBERSHIP_PAYMENT_METHOD_CD, 
 TRIM(CONVERT(CHAR,MPMT_CC_TYP_CD)) AS MEMBERSHIP_PAYMENT_CC_TYPE_CD, 
 TRIM(CONVERT(CHAR,MPMT_CC_EXPIR_DT,121)) AS MEMBERSHIP_PAYMENT_CC_EXPIRE_DT,
 TRIM(CONVERT(CHAR,MPMT_CC_NR)) AS MEMBERSHIP_PAYMENT_CC_NUM, 
 TRIM(CONVERT(CHAR,MPMT_DT,121)) AS MEMBERSHIP_PAYMENT_DT, 
 TRIM(CONVERT(CHAR,MPMT_TRAN_TYP_CD)) AS MEMBERSHIP_PAYMENT_TRAN_TYPE_CD, 
 TRIM(CONVERT(CHAR,MPMT_ADJ_DSC_CD)) AS MEMBERSHIP_PAYMENT_ADJ_DESC_CD, 
 TRIM(CONVERT(CHAR,MPMT_CC_AUTH_NR)) AS MEMBERSHIP_PAYMENT_CC_AUTH_NR, 
 TRIM(CONVERT(CHAR,MPMT_SRC_CD)) AS MEMBERSHIP_PAYMENT_SRC_CD,
 TRIM(CONVERT(CHAR,MPMT_CREAT_DT,121)) AS MEMBERSHIP_PAYMENT_CREATE_DT, 
 TRIM(CONVERT(CHAR,MPMT_WO_AT)) AS MEMBERSHIP_PAYMENT_WO_AMT, 
 TRIM(CONVERT(CHAR,MPMT_CC_OWNR_IN)) AS MEMBERSHIP_PAYMENT_CC_OWNER_IND, 
 TRIM(CONVERT(CHAR,MPMT_BTCH_KY)) AS MEMBERSHIP_PAYMENT_BATCH_KEY, 
 TRIM(CONVERT(CHAR,MPMT_FROM_CD)) AS MEMBERSHIP_PAYMENT_FROM_CD, 
 TRIM(CONVERT(CHAR,MPMT_BILL_KY)) AS MEMBERSHIP_PAYMENT_BILL_KEY,
 TRIM(CONVERT(CHAR,MPMT_TRAN_APPLY_AT)) AS MEMBERSHIP_PAYMENT_TRAN_APPLY_AMT, 
 TRIM(CONVERT(CHAR,MBAL_KY)) AS MEMBERSHIP_BALANCE_KEY, 
 TRIM(CONVERT(CHAR,MBRS_KY)) AS MEMBERSHIP_KEY, 
 TRIM(CONVERT(CHAR,MPMT_REV_PMT_AT)) AS MEMBERSHIP_PAYMENT_REV_PMT_AMT, 
 TRIM(CONVERT(CHAR,MPMT_SAFE_AT)) AS MEMBERSHIP_PAYMENT_SAFE_AMT, 
 TRIM(CONVERT(CHAR,MPMT_CHK_NR)) AS MEMBERSHIP_PAYMENT_CHK_NR,
 TRIM(CONVERT(CHAR,MPMT_TYPE_KY)) AS MEMBERSHIP_PAYMENT_TYPE_KEY, 
 TRIM(CONVERT(CHAR,CUST_RECPT_NR)) AS CUSTOMER_RECIPT_NR, 
 TRIM(CONVERT(CHAR,MPMT_RFND_METH_CD)) AS MEMBERSHIP_PAYMENT_REFUND_METH_CD, 
 TRIM(CONVERT(CHAR,MPMT_DNR_KY)) AS MEMBERSHIP_PAYMENT_DONOR_KEY, 
 TRIM(CONVERT(CHAR,OFC_CD)) AS OFFICE_CD, 
 TRIM(CONVERT(CHAR,POSTED_BY_USERID)) AS POSTED_BY_USERID, 
 TRIM(CONVERT(CHAR,MPMT_CC_NR_KEY_ID)) AS MEMBERSHIP_PAYMENT_CC_NR_KEY_ID, 
 TRIM(CONVERT(CHAR,MPMT_CC_SETTLED_IN)) AS MEMBERSHIP_PAYMENT_CC_SETTLED_IND, 
 TRIM(CONVERT(CHAR,MPMT_CANCL_IN_PROG_IN)) AS MEMBERSHIP_PAYMENT_CANCEL_IN_PROG_IND, 
 TRIM(CONVERT(CHAR,MPMT_EXTERN_ID)) AS MEMBERSHIP_PAYMENT_EXTERN_ID, 
 TRIM(CONVERT(CHAR,MPMT_REV_KY)) AS MEMBERSHIP_PAYMENT_REV_KEY, 
 TRIM(CONVERT(CHAR,BPMT_KY))  AS BILL_PAYMENT_KEY, 
 TRIM(CONVERT(CHAR,SERVICE_CHARGE)) AS SERVICE_CHARGE  
FROM
 "v_database_name".dbo.MBRSHIP_PAYMENT
WHERE CONVERT(date,MPMT_CREAT_DT) >= DATEADD(day,-1,convert(date, 'v_inputdate'))
AND CONVERT(date,MPMT_CREAT_DT) < CONVERT(date,'v_inputdate');