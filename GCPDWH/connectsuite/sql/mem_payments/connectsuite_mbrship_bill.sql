STG.BILL_REPRT_REQ_IN,
STG.BILL_FST_IN,
STG.BILL_TYP_CD,
STG.EMAILED_IND,
STG.MBRS_EXPIR_DT,
'jobrunid',
'CONNECT SUITE',
CURRENT_DATETIME(),
CURRENT_DATETIME()
) 