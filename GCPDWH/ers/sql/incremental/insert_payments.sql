
SELECT TO_CHAR(PAYMENT.PAY_ID) PAY_ID, 
TO_CHAR(PAYMENT.CHK_ID) CHK_ID, 
TO_CHAR(PAYMENT.CLB_CD) CLB_CD, 
TO_CHAR(PAYMENT.MBR_ID) MBR_ID, 
TO_CHAR(PAYMENT.ASSOC_MBR_ID) ASSOC_MBR_ID, 
TO_CHAR (PAYMENT.PAY_DATE,'YYYY-MM-DD HH24:MI:SS') AS PAY_DATE,
TO_CHAR(PAYMENT.PAY_TYPE) PAY_TYPE, 
TO_CHAR(PAYMENT.CHECK_STUB_CD) CHECK_STUB_CD, 
TO_CHAR(PAYMENT.FACIL_INT_ID) FACIL_INT_ID, 
TO_CHAR(PAYMENT.PAY_METHOD) PAY_METHOD, 
TO_CHAR(PAYMENT.PAY_AMOUNT) PAY_AMOUNT, 
TO_CHAR (PAYMENT.PAY_PERIOD_START, 'YYYY-MM-DD HH24:MI:SS') AS PAY_PERIOD_START,
TO_CHAR (PAYMENT.PAY_PERIOD_END, 'YYYY-MM-DD HH24:MI:SS') AS PAY_PERIOD_END,
TO_CHAR(PAYMENT.PAY_EIN) PAY_EIN, 
TO_CHAR(PAYMENT.PAID_IND) PAID_IND, 
TO_CHAR(PAYMENT.TRANS_NUM) TRANS_NUM, 
TO_CHAR(PAYMENT.PAY_BASIC) PAY_BASIC, 
TO_CHAR(PAYMENT.PAY_PLUS) PAY_PLUS, 
TO_CHAR(PAYMENT.PAY_DEBIT_ADJ) PAY_DEBIT_ADJ, 
TO_CHAR(PAYMENT.PAY_RBASIC) PAY_RBASIC, 
TO_CHAR(PAYMENT.PAY_RPLUS) PAY_RPLUS, 
TO_CHAR(PAYMENT.PAY_PLUSRV) PAY_PLUSRV, 
TO_CHAR(PAYMENT.PAY_TAX1) PAY_TAX1, 
TO_CHAR(PAYMENT.PAY_TAX2) PAY_TAX2, 
TO_CHAR(PAYMENT.PAY_CREDIT_ADJ) PAY_CREDIT_ADJ, 
TO_CHAR(PAYMENT.PAY_PREM) PAY_PREM, 
TO_CHAR(PAYMENT.PAY_PRRV) PAY_PRRV, 
TO_CHAR(PAYMENT.PAY_RPREM) PAY_RPREM, 
TO_CHAR(PAYMENT.PAY_RPRRV) PAY_RPRRV, 
TO_CHAR(PAYMENT.PAY_OTHER) PAY_OTHER
,'jobrunid' AS JOB_RUN_ID,
'jobname' AS CREATED_BY,
'ers' AS SOURCE_SYSTEM_CD
FROM
NCA_APD.PAYMENT PAYMENT
WHERE  PAY_DATE>= TO_DATE('incr_stage_payment','yyyy-mm-dd hh24:mi:ss')