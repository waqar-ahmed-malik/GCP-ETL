ACCOUNT_ID,
ACCOUNT_TYPE,
CUST_TYPE,
SHORT_CD,
SORT_NM,
NAME,
ADDR1,
ADDR2,
ADDR3,
CITY,
STATE,
ZIP,
COUNTRY,
EMAIL,
BUS_PHN,
FAX_PHN,
HOME_PHN,
