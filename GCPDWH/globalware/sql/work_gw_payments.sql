SELECT        
TRIM(CONVERT(CHAR,ID)) AS ID,
TRIM(CONVERT(CHAR,"Source")) AS  PAYMENT_SOURCE,
TRIM(CONVERT(CHAR,AccountId )) AS ACCOUNT_ID,
TRIM(CONVERT(CHAR,Provider  )) AS PROVIDER,
TRIM(CONVERT(CHAR,CustDueDate,120)) AS CUST_DUE_DT,
TRIM(CONVERT(CHAR,CustRcvdAmt)) AS CUST_RCVD_AMT,
TRIM(CONVERT(CHAR,CustDueAmt)) AS CUST_DUE_AMT,
TRIM(CONVERT(CHAR,CustRcvdFlag)) AS CUST_RCVD_FLG,
TRIM(CONVERT(CHAR,CustRcvdDate,120)) AS CUST_RCVD_DT,
TRIM(CONVERT(CHAR,ProvDueDate,120)) AS  PROV_DUE_DT,
TRIM(CONVERT(CHAR,ProvDueAmount)) AS PROV_DUE_AMT,
TRIM(CONVERT(CHAR,ProvPaidAmount)) AS PROV_PAID_AMT,
TRIM(CONVERT(CHAR,ProvPaidDate,120)) AS PROV_PAID_DT,
TRIM(CONVERT(CHAR,ProvPaidFlag)) AS PROV_PAID_FLG,
TRIM(CONVERT(CHAR,CommDueDate,120)) AS COMM_DUE_DT,
TRIM(CONVERT(CHAR,CommDueAmount)) AS COMM_DUE_AMT,
TRIM(CONVERT(CHAR,CommRcvdAmount)) AS COMM_RCVD_AMT,
TRIM(CONVERT(CHAR,CommRcvdDate,120)) AS COMM_RCVD_DT,
TRIM(CONVERT(CHAR,CommRcvdFlag)) AS COMM_RCVD_FLG,
TRIM(CONVERT(CHAR,replace(replace("Comment", char(13), ''),char(10),''))) AS  PAYMENT_COMMENT,          
TRIM(CONVERT(CHAR,Property  )) AS PROPERTY,
TRIM(CONVERT(CHAR,Tag)) AS TAG,
TRIM(CONVERT(CHAR,TempAmount)) AS TEMP_AMT,
TRIM(CONVERT(CHAR,GlBranch)) AS GL_BRANCH,
TRIM(CONVERT(CHAR,OrigRcptNum)) AS ORIG_RCPT_NUM,
TRIM(CONVERT(CHAR,PrevBilled)) AS PREV_BILLED,
TRIM(CONVERT(CHAR,GroupID)) AS GROUP_ID,
TRIM(CONVERT(CHAR,CreateDateTime,120)) AS CREATE_DTTIME 
FROM DBA.Payments
WHERE  CONVERT(DATE,CreateDateTime) >= CONVERT(DATE,'v_startdate')